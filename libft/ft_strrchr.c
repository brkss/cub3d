char	*ft_strrchr(const char *str, int c)
{
	int		i;
	char	*s;
	char	*p;

	s = (char *)(str);
	i = 0;
	p = 0;
	while (s[i] != '\0')
	{
		if (s[i] == (char)c)
			p = &s[i];
		i++;
	}
	if ((char)c == s[i])
		p = &s[i];
	return (p);
}
